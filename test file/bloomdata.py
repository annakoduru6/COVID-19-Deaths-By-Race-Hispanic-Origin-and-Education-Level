'''This function will add one (1) to the value called.'''

def increment_num(num):
    return num + 1

'''This function will multiply the number called against itself.'''

def multiply_num(num):
    return num * num

'''This function will subtract one (1) to the value called.'''

def subtract_num(num):
    return num - 1

'''This function will return the square root of the number called. '''

def sq_rt_num(num):
    return num ** 2
